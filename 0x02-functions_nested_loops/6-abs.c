#include "main.h"
#include <stdlib.h>

/**
 * _abs - Calculate the absolute value of an integer.
 * @n: The integer for which the
 * absolute value should be computed.
 *
 * Description:
 * This function takes an integer 'n'
 * as input and calculates its absolute value.
 * It returns the absolute value of 'n'
 * by utilizing the 'abs' function from the C standard library.
 *
 * @n: The integer for which the absolute value should be computed.
 *
 * Return: The absolute value of the input integer.
 */
int _abs(int n)
{
return (abs(n));
}
